"""
Контентный сервис: AI-задачи поверх Claude.

Отвечает за:
- генерацию улучшенных названий и описаний товаров
- ответы на отзывы покупателей
- рекомендации по ценам
- общие текстовые задачи в стиле бренда
"""
from __future__ import annotations

import json
from typing import Any

from services.claude_service import ClaudeService


# Голос бренда — RBR (кофейня/кофе). Можешь подправить под свой стиль.
BRAND_VOICE = (
    "Бренд RBR — обжарщик специальности. Тон: тёплый, экспертный, без воды. "
    "Уважительное «вы». Без восклицательных знаков, без CAPS, без эмодзи в карточках. "
    "Эмодзи можно только в ответах на отзывы, и то 1-2 максимум."
)


class ContentService:
    """AI-обёртка над типовыми задачами магазина."""

    def __init__(self, claude: ClaudeService | None = None):
        self.claude = claude or ClaudeService()

    # ------------------------------------------------------------------ #
    # SEO: название и описание
    # ------------------------------------------------------------------ #

    async def improve_product_card(
        self,
        current_name: str,
        current_description: str,
        offer_id: str = "",
        category_hint: str = "",
    ) -> dict[str, str]:
        """
        Улучшить название и описание карточки.
        Возвращает {"name": "...", "description": "...", "rationale": "..."}.
        """
        system = (
            f"{BRAND_VOICE}\n\n"
            "Ты — SEO-копирайтер для маркетплейса OZON. "
            "Задача: переписать название и описание товара так, чтобы:\n"
            "1) Название содержало бренд + ключевую характеристику + объём/вес/размер. "
            "Длина 50-120 символов. Только продающие ключи, без штампов.\n"
            "2) Описание было структурированным: 1 абзац-крючок (что это и для кого), "
            "затем характеристики списком, затем 1 короткий абзац о применении/уходе. "
            "Длина 400-1500 символов. Без воды и маркетинговых клише.\n"
            "3) Не использовать запрещённые на OZON слова: «лучший», «№1», «уникальный».\n\n"
            "Верни СТРОГО JSON, без обрамления ```json:\n"
            '{"name": "...", "description": "...", "rationale": "краткое объяснение что улучшил"}'
        )
        user = (
            f"Артикул: {offer_id or '—'}\n"
            f"Категория-подсказка: {category_hint or '—'}\n\n"
            f"Текущее название:\n{current_name}\n\n"
            f"Текущее описание:\n{current_description or '(пусто)'}"
        )

        raw = await self.claude.ask(user, system=system, max_tokens=2000)
        return _parse_json(raw, fallback={"name": current_name, "description": current_description, "rationale": "Не удалось распарсить ответ AI"})

    # ------------------------------------------------------------------ #
    # Ответы на отзывы
    # ------------------------------------------------------------------ #

    async def reply_to_review(
        self,
        review_text: str,
        rating: int,
        product_name: str = "",
    ) -> str:
        """
        Сгенерировать ответ на отзыв.
        Тон зависит от оценки: 1-3 — извинение и решение, 4 — благодарность с просьбой
        указать что улучшить, 5 — тёплая благодарность.
        """
        if rating <= 3:
            tone_hint = (
                "Покупатель недоволен. Признай проблему, извинись без оправданий, "
                "предложи конкретное решение (замена, возврат, написать в личку магазина). "
                "Не спорь с фактами. Без эмодзи."
            )
        elif rating == 4:
            tone_hint = (
                "Покупатель в целом доволен. Поблагодари, мягко спроси что бы он улучшил. "
                "Можно 1 эмодзи (☕ уместно для кофе)."
            )
        else:
            tone_hint = (
                "Покупатель в восторге. Тёплая благодарность, без шаблона. "
                "Можно 1-2 эмодзи (☕ ❤️). 2-4 предложения, не больше."
            )

        system = (
            f"{BRAND_VOICE}\n\n"
            "Ты пишешь публичный ответ на отзыв на OZON от лица магазина. "
            f"{tone_hint}\n"
            "Длина 200-500 символов. Без подписи в конце (OZON добавляет автоматически)."
        )
        user = (
            f"Товар: {product_name or '—'}\n"
            f"Оценка: {rating}/5\n"
            f"Текст отзыва:\n{review_text}"
        )
        return (await self.claude.ask(user, system=system, max_tokens=600)).strip()

    # ------------------------------------------------------------------ #
    # Аналитика и сводки
    # ------------------------------------------------------------------ #

    async def make_daily_summary(self, summary_data: dict[str, Any]) -> str:
        """Утренняя сводка магазина в Markdown (Telegram-совместимом)."""
        system = (
            "Ты — аналитик магазина OZON. Получаешь JSON с показателями за день. "
            "Сделай сводку в Markdown для Telegram. "
            "Используй *жирный* (одна звёздочка), _курсив_ (подчёркивания), `моноширинный`. "
            "НЕ используй ## заголовки и **двойные звёздочки** — Telegram их не понимает. "
            "Структура: краткое резюме → ключевые цифры → 2-3 наблюдения → 2-3 рекомендации. "
            "Эмодзи в меру (1-2 на блок). Длина не больше 1500 символов."
        )
        user = (
            f"Данные:\n```json\n{json.dumps(summary_data, ensure_ascii=False, indent=2)}\n```"
        )
        return await self.claude.ask(user, system=system, max_tokens=1500)


# --------------------------------------------------------------------------- #
# Хелперы
# --------------------------------------------------------------------------- #

def _parse_json(raw: str, fallback: dict[str, Any]) -> dict[str, Any]:
    """Достать JSON из ответа Claude, даже если он завернул его в ```json."""
    text = raw.strip()
    # Снимаем возможные ограждения
    if text.startswith("```"):
        # Убираем первую и последнюю строки с ```
        lines = text.split("\n")
        if len(lines) >= 2:
            text = "\n".join(lines[1:-1])
    try:
        return json.loads(text)
    except json.JSONDecodeError:
        return fallback
